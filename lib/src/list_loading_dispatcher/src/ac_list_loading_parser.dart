import 'package:equatable/equatable.dart';

/// Нормализованный результат парсера загрузки списка.
///
/// Содержит элементы, извлечённые из ответа loader'а, и флаг продолжения
/// [hasMore]. Возвращается функцией типа [ACListLoadingParser] и используется
/// диспатчером для обновления внутреннего состояния.
///
/// Пустой [items] допустим: означает, что текущий запрос ничего не вернул,
/// но [hasMore] определяет, имеет ли смысл делать следующую страницу.
class ACParseResult<T> extends Equatable {
  /// Создаёт результат парсера.
  ///
  /// [items] — извлечённые элементы (может быть пустым).
  /// [hasMore] — есть ли ещё элементы для догрузки после текущего ответа.
  const ACParseResult({
    required this.items,
    required this.hasMore,
  });

  /// Элементы, извлечённые парсером из ответа loader'а.
  final List<T> items;

  /// Признак наличия следующей страницы.
  ///
  /// `true` — следующий вызов loader'а вернёт новые элементы; `false` —
  /// дальнейшая пагинация невозможна.
  final bool hasMore;

  @override
  List<Object?> get props => [items, hasMore];
}

/// Синхронная функция, преобразующая ответ loader'а в [ACParseResult].
///
/// Диспатчер вызывает парсер после того, как сам дождался `Future<R>` от
/// loader'а, передавая ему уже готовый ответ. Парсер должен быть чистым и
/// синхронным — он не возвращает `Future`.
///
/// Исключения, брошенные парсером, перехватываются диспатчером в общем
/// `try/catch` и попадают в состояние как ошибка загрузки (поле `error`).
///
/// Парсер не имеет доступа к предыдущему состоянию диспатчера. Любую
/// контекстную информацию (например, текущий cursor для пагинации)
/// потребитель должен пробрасывать через замыкание.
typedef ACListLoadingParser<T, R> = ACParseResult<T> Function(R response);
