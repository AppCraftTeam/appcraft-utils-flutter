/// Контракт параметров загрузки, принимаемых диспатчером списка.
///
/// Пользовательский тип параметров загрузки (передаваемый в `reload` и
/// `loadMore`) должен подмешать этот миксин. Диспатчер читает только
/// [query] — он участвует в логике `ACSearchStrategy` (debounce, minLength,
/// сброс при пустом значении). Остальные поля ([limit] и [offset]) —
/// информационные и предназначены для удобства потребителя при
/// формировании запросов внутри собственного loader'а.
///
/// Типичное использование:
///
/// ```dart
/// final class UserListParams with ACListLoadingParamsMixin {
///   const UserListParams({this.offset, this.limit, this.query});
///
///   @override
///   final int? offset;
///   @override
///   final int? limit;
///   @override
///   final String? query;
/// }
/// ```
mixin ACListLoadingParamsMixin {
  /// Максимальное количество элементов, которое loader должен вернуть.
  ///
  /// Информационное поле: диспатчер его не читает. Рекомендуется значение
  /// `>= 0`; валидация остаётся на стороне потребителя.
  int? get limit;

  /// Смещение (offset) для offset-based пагинации.
  ///
  /// Информационное поле: диспатчер его не читает. Рекомендуется значение
  /// `>= 0`; валидация остаётся на стороне потребителя.
  int? get offset;

  /// Поисковый запрос; основа поведения `ACSearchStrategy`.
  ///
  /// Диспатчер трактует `null` и пустую строку эквивалентно — как отсутствие
  /// поиска. Обрезка пробелов (trim) — ответственность потребителя.
  String? get query;
}
